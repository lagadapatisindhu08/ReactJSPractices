let array = ["Apple", "Banana", "Orange"];

array.push("Graphes"); // Added to last item in array
console.log(array);

array.pop(); // Remove last item in array
console.log(array);

array.unshift("PineApple"); // Added to first item in array
console.log(array);

array.shift(); // Remove first item in array 
console.log(array);
